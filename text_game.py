print("Добро пожаловать в приключение!")
print("Вы находитесь в темном лесу. Что будете делать?")

choice = input("1. Пойти налево. 2. Пойти направо. 3. Остаться на месте.")

if choice == "1":
    print("Вы идете налево и встречаете злого волка.")
    action = input("1. Атаковать волка. 2. Убежать.")
    if action == "1":
        print("Вы атакуете волка, но он оказывается сильнее и убивает вас.")
    elif action == "2":
        print("Вы убегаете от волка и находите выход из леса. Поздравляем, вы выжили!")
    else:
        print("Некорректный выбор. Вы проиграли.")
elif choice == "2":
    print("Вы идете направо и находите старинный замок.")
    action = input("1. Войти в замок. 2. Обойти замок и продолжить путь.")
    if action == "1":
        print("Вы заходите в замок и встречаете злого мага.")
        fight = input("1. Сразиться с магом. 2. Спрятаться и ждать, пока маг уйдет.")
        if fight == "1":
            print("Вы сражаетесь с магом, но он оказывается сильнее и убивает вас.")
        elif fight == "2":
            print("Вы спрячетесь и ждете, пока маг уйдет. Затем находите выход из замка. Поздравляем, вы выжили!")
        else:
            print("Некорректный выбор. Вы проиграли.")
    elif action == "2":
        print("Вы обходите замок и продолжаете путь. Ничего интересного не происходит.")
    else:
        print("Некорректный выбор. Вы проиграли.")
elif choice == "3":
    print("Вы остаетесь на месте и ничего не происходит.")
else:
    print("Некорректный выбор. Вы проиграли.")